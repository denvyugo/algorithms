a1 = ord(input('введите первую латинскую букву '))
a2 = ord(input('введите вторую латинскую букву '))

q1 = a1 - ord('a') + 1
q2 = a2 - ord('a') + 1
q = a2 - a1 - 1

print(f'номер первой буквы {chr(a1)}: {q1}')
print(f'номер второй буквы {chr(a2)}: {q2}')
print(f'число букв между {chr(a1)} и {chr(a2)}: {q}')
